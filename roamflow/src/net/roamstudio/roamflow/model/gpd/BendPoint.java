/**
 * @author chinakite zhang
 *
 */
public class BendPoint {
	private int w1;
	private int h1;
	private int w2;
	private int h2;
	/**
	 * @return the h1
	 */
	public int getH1() {
		return h1;
	}
	/**
	 * @param h1 the h1 to set
	 */
	public void setH1(int h1) {
		this.h1 = h1;
	}
	/**
	 * @return the h2
	 */
	public int getH2() {
		return h2;
	}
	/**
	 * @param h2 the h2 to set
	 */
	public void setH2(int h2) {
		this.h2 = h2;
	}
	/**
	 * @return the w1
	 */
	public int getW1() {
		return w1;
	}
	/**
	 * @param w1 the w1 to set
	 */
	public void setW1(int w1) {
		this.w1 = w1;
	}
	/**
	 * @return the w2
	 */
	public int getW2() {
		return w2;
	}
	/**
	 * @param w2 the w2 to set
	 */
	public void setW2(int w2) {
		this.w2 = w2;
	}
}
